<h3>Как сделать заказ через систему</h3>
<p style="text-align: right;">
 <a href="/docs/zakaz_123.pdf" target="_blank" class="btn btn-default">Скачать этот текст</a>
</p>
<ol>
	<li>
	<p style="text-align: justify;">
 <span style="font-size: 12pt;">Составить перечень учебников вашей образовательной организации. Данный перечень должен соответствовать требованиям реализации основной образовательной программы школы.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		Внимательно проверяйте &nbsp;автора и издательство, которое выпускает нужный вам учебник! Это важно, так как &nbsp;наличие разных УМК у одних и тех же авторов может привести к неверно оформленному заказу! Например: автор Климанова Л.Ф. Литературное чтение. Издательство «Просвещение» имеет УМК в «Школе России» и «Перспективе». Проверяйте по соавторам. Автор: Климанова Л. Ф., Горецкий В.Г., Голованова М.В. и др.&nbsp; – «Школа России»,&nbsp; автор: Климанова Л. Ф., Горецкий В.Г., Виноградская Л.А.&nbsp; – «Перспектива».</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		Аналогичная история с автором &nbsp;Макарычевым Ю.Н.: УМК по алгебре 7-9 класс есть в издательстве «Просвещение» и в издательстве «Мнемозина» (для углубленного изучения предмета).</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		Составленный перечень учебников вашей ОО должен соответствовать Федеральному перечню учебников (ФПУ) (<a href="/docs/fpu/prikaz_253_31-03-2014.pdf" target="_blank">приказ от 31 марта 2014 г. № 253</a>, <a href="/docs/fpu/prikaz_576_08-06-2015.pdf" target="_blank">приказ от 8 июня 2015 г. № 576</a>), одобрен метод.советом / педсоветом или тем, кто определён в локальном акте ОО, и подписан директором школы. </span>
	</p>
 <span style="font-size: 12pt;"> </span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li style="text-align: justify;" value="2"><span style="font-size: 12pt;">
	Определить сумму средств, которую школа планирует направить на закупку учебников, определить количество экземпляров учебников каждого наименования. В зависимости от суммы заказа школа может выбрать один из пунктов части 1 статьи 93 ФЗ-44 (п.4 – сумма до 100 тыс.руб., п.5 – сумма до 400 тыс. руб, п.14 – любая сумма заказа). Подробнее см. документ </span><a href="/docs/norm_basa.pdf" target="_blank"><span style="font-size: 12pt;">«Нормативная база»</span></a><span style="font-size: 12pt;">. Данная информация заполняется в карточке школы!</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="3"><span style="font-size: 12pt;">
	Зайти на сайт </span><a href="/" target="_blank"><span style="font-size: 12pt;">http://book.oblcit.ru</span></a><span style="font-size: 12pt;">&nbsp; в личный кабинет школы, набрав логин и пароль, полученный у оператора системы – ГБУ ДПО НСО «ОблЦИТ». И далее действовать согласно </span><a target="_blank" href="/docs/metod.pdf"><span style="font-size: 12pt;">методическим рекомендациям по работе с АИС</span></a><a href="/docs/metod.pdf" target="_blank"><span style="font-size: 12pt;">.</span></a><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
	Если у Вас нет логина и пароля или Вы их потеряли, Вам необходимо обратиться к муниципальному координатору системы, если вопрос решить не смогли, то – к региональному оператору с просьбой выслать учётные данные Вашей школы.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
	Адреса региональных координаторов:</span><a href="mailto:viv@oblcit.ru"><span style="font-size: 12pt;"> viv@oblcit.ru</span></a><span style="font-size: 12pt;"> &nbsp;(Вержанская Валентина Ильинична), </span><a href="mailto:amu@oblcit.ru"><span style="font-size: 12pt;">amu@oblcit.ru</span></a><span style="font-size: 12pt;"> (Асеева Марина Юрьевна).</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span></li>
	<li><span style="font-size: 12pt;">В личном кабинете на сайте </span><a href="/" target="_blank"><span style="font-size: 12pt;">http://book.oblcit.ru</span></a><span style="font-size: 12pt;"> заполнить карточку школы, внести все необходимые реквизиты.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span><b><span style="font-size: 12pt;">Будьте внимательны: информация из карточки школы автоматически заносится в договор/контракт на поставку учебников!</span></b><span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="5"><span style="font-size: 12pt;">
	Можно приступать к формированию заказа на учебники по каталогам издательств в АИС, следуя </span><a target="_blank" href="/docs/metod.pdf"><span style="font-size: 12pt;">методическим рекомендациям по работе с АИС</span></a><a href="/docs/metod.pdf" target="_blank"><span style="font-size: 12pt;">.</span></a></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="6"><span style="font-size: 12pt;">
	Сформированный и проверенный заказ отправить оператору. Для этого достаточно нажать кнопку «Отправить заказ».</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="7"><span style="font-size: 12pt;">
	Оператор системы, получив заказ от школы, формирует пакет документов по каждому издательству для каждой школы и отправляет в электронном виде&nbsp; в адрес школы и в адрес издательства для согласования.</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="8"><span style="font-size: 12pt;">
	Когда документы согласованы обеими сторонами (школа - издательство), издательства отправляют в адрес оператора системы бумажные формы договоров с синими печатями. Оператор рассылает их по муниципалитетам или непосредственно по школам.</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="9"><span style="font-size: 12pt;">
	Школы подписывают присланные документы, один экземпляр оставляют себе, второй – отправляют почтой России в издательство, а сканированные листы с подписями и печатями – в электронном виде высылают оператору системы для контроля.</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="10"><span style="font-size: 12pt;">
	Издательства начинают формировать заказы для школ. Теперь с системе заказы имеют статус «На исполнении», школа ожидает прибытие груза в соответствии условиями договора.</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="11"><span style="font-size: 12pt;">
	&nbsp;Книги могут быть доставлены в школу или муниципалитет (по решению муниципалитета) несколькими способами:</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span></li>
	<ul>
		<li value="11"><span style="font-size: 12pt;">логистической (транспортной) компанией, с которой издательство заключает договор на доставку груза.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		Представитель компании связывается с ответственным в ОО за получение груза и сообщает о времени и месте доставки книг.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		В случае больших объёмов поставок не забывайте заказывать грузчиков! Их услуги входят в доставку.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span></li>
		<li value="11"><span style="font-size: 12pt;">почтой России.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;"> </span></li>
		<li value="11"><span style="font-size: 12pt;">курьерской почтой.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
		Второй и третий варианты - для небольших объёмов поставок. </span></li>
	</ul>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li style="text-align: justify;" value="12"><span style="font-size: 12pt;">
	&nbsp;Когда учебники получены, ответственный сотрудник ОО проверяет выполненный заказ на соответствие условиям договора, в случае разногласий составляет претензию в издательство, направляет по электронной почте в адрес издательства и оператора системы.</span><br>
 <span style="font-size: 12pt;"> </span><br>
 <span style="font-size: 12pt;">
	Если всё нормально, директор подписывает накладные и спецификации, ставит синие печати, один экземпляр документов оставляет себе, второй - &nbsp;отправляет в издательство почтой России. Статус заказа становится «Исполнен». </span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="13"><span style="font-size: 12pt;">
	&nbsp;Школа оплачивает заказ издательству согласно договору/контракту. Заказ переходит в статус «Закрыт». Теперь его можно только просматривать.</span></li>
</ol>
 <span style="font-size: 12pt;"> </span>
<ol>
	<li value="14"><span style="font-size: 12pt;">
	&nbsp;В случае, если школа осуществляла закупки &nbsp;учебников &nbsp;иным способом, необходимо заполнить базу закупленных учебников &nbsp;на сайте </span><a href="/" target="_blank"><span style="font-size: 12pt;">http://book.oblcit.ru</span></a><span style="font-size: 12pt;"> &nbsp;в режиме «отчёта»! См. </span><a target="_blank" href="/docs/metod.pdf"><span style="font-size: 12pt;">методическим рекомендациям по работе с АИС.</span></a></li>
</ol>